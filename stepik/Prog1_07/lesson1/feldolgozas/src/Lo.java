public class Lo {
    private String szin;
    private int sebesseg;

    public Lo(String szin, int sebesseg) {
        this.szin = szin;
        this.sebesseg = sebesseg;
    }

    public String getSzin() {
        return szin;
    }

    public void setSzin(String szin) {
        this.szin = szin;
    }

    public int getSebesseg() {
        return sebesseg;
    }

    public void setSebesseg(int sebesseg) {
        this.sebesseg = sebesseg;
    }

}
